    private void tabelaClientesMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClientesMouseReleased
       tabelaClientes.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
           @Override
           
         
           public void valueChanged(ListSelectionEvent e) {
                 if (!e.getValueIsAdjusting() && tabelaClientes.getSelectedRow() != -1) {
             
               int selectedRow = tabelaClientes.getSelectedRow();
                txtCodigo.setText(tabelaClientes.getValueAt(selectedRow, 0).toString());
                cbTipoPessoa.setSelectedItem((String) tabelaClientes.getValueAt(selectedRow, 1).toString());
                txtnome.setText(tabelaClientes.getValueAt(selectedRow, 2).toString());
                txtrg.setText(tabelaClientes.getValueAt(selectedRow, 3).toString());
                txtcpf.setText(tabelaClientes.getValueAt(selectedRow, 4).toString());
                txtCnpj.setText( tabelaClientes.getValueAt(selectedRow, 5).toString());  
                txtdatanasc.setText( tabelaClientes.getValueAt(selectedRow, 6).toString());
                txtcel.setText(tabelaClientes.getValueAt(selectedRow, 7).toString());
                txtcep.setText(tabelaClientes.getValueAt(selectedRow, 8).toString());
                txtend.setText(tabelaClientes.getValueAt(selectedRow, 9).toString());
                txtbairro.setText(tabelaClientes.getValueAt(selectedRow, 10).toString());
                cbCidade.setSelectedItem(tabelaClientes.getValueAt(selectedRow, 11).toString());
                cbuf.setSelectedItem(tabelaClientes.getValueAt(selectedRow, 12).toString());
                jTabbedPane5.setSelectedComponent(painel_dados5);
                btnEditar.setEnabled(true);
                btnExcluir.setEnabled(true);
                btnCancelar.setEnabled(false);
           }
           
           }
       
    
    });
    }//GEN-LAST:event_tabelaClientesMouseReleased

