    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // boto salvar
            Clientes obj = new Clientes();
            obj.setTipoPessoa((String)cbTipoPessoa.getSelectedItem());
            obj.setNome(txtnome.getText());
            obj.setRg(txtrg.getText());
            obj.setCpf(txtcpf.getText());
            obj.setCnpj(txtCnpj.getText());
            obj.setDataNasc(txtdatanasc.getText());
            obj.setEndereco(txtend.getText());
            obj.setBairro(txtbairro.getText());
            obj.setCidade((String)cbCidade.getSelectedItem());
            obj.setCep(Integer.parseInt(txtcep.getText()));
            obj.setEstado((String)cbuf.getSelectedItem());
            obj.setCelular(txtcel.getText());

            ClasseCliente dao = new ClasseCliente();

        try {
            dao.cadastrarCliente(obj);
            listarClientes();
             btnCancelar.setEnabled(false);
              btnsalvar.setEnabled(true);
              btnEditar.setEnabled(false);
                btnnovo.setEnabled(false);
                 btnExcluir.setEnabled(false);
                limpaCampos();
                desabilitarCampos();
            
             jTabbedPane5.setEnabledAt(0, true);

        } catch (SQLException ex) {
            Logger.getLogger(CadCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

            new Utilitarios().LimpaTela(painel_dados5);

    }//GEN-LAST:event_btnsalvarActionPerformed

